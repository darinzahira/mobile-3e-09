package com.example.goodang.profile;

public class Profile {
    private String nama, email, nomer, alamat;

    public Profile(String nama, String email, String nomer, String alamat) {
        this.nama = nama;
        this.email = email;
        this.nomer = nomer;
        this.alamat = alamat;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNomer() {
        return nomer;
    }

    public void setNomer(String nomer) {
        this.nomer = nomer;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
}
